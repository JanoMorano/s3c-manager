CREATE OR REPLACE VIEW v_c3capabilitymaphierarchyexport AS
WITH mapped AS (
    SELECT DISTINCT c3_uuid
    FROM service_c3_mapping
    WHERE c3_uuid IS NOT NULL
),
mapping_counts AS (
    SELECT c3_uuid, COUNT(*) AS mapping_count
    FROM service_c3_mapping
    WHERE c3_uuid IS NOT NULL
    GROUP BY c3_uuid
)
SELECT
    c.uuid,
    c.external_id,
    c.title,
    c.item_type,
    c.application,
    c.item_status,
    c.parent_uuid,
    c.parent_code,
    p.title AS parent_title,
    p.external_id AS parent_external_id,
    CASE WHEN m.c3_uuid IS NULL THEN FALSE ELSE TRUE END AS is_mapped,
    COALESCE(mc.mapping_count, 0) AS mapping_count,
    c.order_num,
    c.synced_at
FROM c3_taxonomy c
LEFT JOIN c3_taxonomy p ON p.uuid = c.parent_uuid
LEFT JOIN mapped m ON m.c3_uuid = c.uuid
LEFT JOIN mapping_counts mc ON mc.c3_uuid = c.uuid;

