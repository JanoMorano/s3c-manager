    'c3' AS source_kind,
    parent.uuid AS source_id,
    COALESCE(parent.title, parent.external_id, parent.uuid) AS source_label,
    'c3' AS target_kind,
    child.uuid AS target_id,
    COALESCE(child.title, child.external_id, child.uuid) AS target_label,
    'parent_child' AS edge_kind,
    NULL::VARCHAR(50) AS mapping_type_code,
    FALSE AS is_primary
FROM c3_taxonomy child
JOIN c3_taxonomy parent ON parent.uuid = child.parent_uuid

UNION ALL

SELECT
    'service' AS source_kind,
    sc.service_id AS source_id,
    COALESCE(sc.title, sc.service_id) AS source_label,
    'c3' AS target_kind,
    scm.c3_uuid AS target_id,
    COALESCE(c.title, c.external_id, scm.c3_uuid) AS target_label,
    'service_mapping' AS edge_kind,
    scm.mapping_type_code,
    scm.is_primary
FROM service_c3_mapping scm
JOIN service_catalog sc ON sc.id = scm.service_id AND sc.is_deleted = FALSE
LEFT JOIN c3_taxonomy c ON c.uuid = scm.c3_uuid;

