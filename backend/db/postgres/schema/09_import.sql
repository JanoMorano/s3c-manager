CREATE TABLE IF NOT EXISTS import_batch (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    filename       VARCHAR(500) NOT NULL,
    imported_at    TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP,
    imported_by    VARCHAR(255) NULL,
    row_count      INTEGER      NOT NULL DEFAULT 0,
    ok_count       INTEGER      NOT NULL DEFAULT 0,
    warn_count     INTEGER      NOT NULL DEFAULT 0,
    error_count    INTEGER      NOT NULL DEFAULT 0,
    parser_version VARCHAR(50)  NOT NULL DEFAULT '1.0',
    source_hash_sha256 VARCHAR(64) NULL,
    notes          TEXT         NULL
);

CREATE TABLE IF NOT EXISTS import_row (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    batch_id   BIGINT       NOT NULL REFERENCES import_batch(id),
    row_number INTEGER      NOT NULL,
    service_id VARCHAR(50)  NULL,
    raw_json   TEXT         NULL,
    status     VARCHAR(20)  NOT NULL CHECK (status IN ('ok', 'warn', 'error', 'skipped', 'processing')),
    created_at TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS import_issue (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    batch_id   BIGINT       NOT NULL REFERENCES import_batch(id),
    row_id     BIGINT       NULL REFERENCES import_row(id),
    service_id VARCHAR(50)  NULL,
    severity   VARCHAR(10)  NOT NULL CHECK (severity IN ('error', 'warn', 'info')),
    issue_code VARCHAR(100) NOT NULL,
    field_name VARCHAR(100) NULL,
    raw_value  TEXT         NULL,
    message    TEXT         NULL,
    resolved   BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS import_contract_report (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_name             VARCHAR(500) NOT NULL,
    source_kind             VARCHAR(50)  NOT NULL,
    created_by              VARCHAR(255) NULL,
    contract_version        VARCHAR(50)  NOT NULL DEFAULT '2026-03-30.import-v1',
    source_hash_sha256      VARCHAR(64)  NULL,
    item_count              INTEGER      NOT NULL DEFAULT 0,
    flavour_count           INTEGER      NOT NULL DEFAULT 0,
    explicit_relation_count INTEGER      NOT NULL DEFAULT 0,
    raw_prerequisite_count  INTEGER      NOT NULL DEFAULT 0,
    missing_target_count    INTEGER      NOT NULL DEFAULT 0,
    stub_count              INTEGER      NOT NULL DEFAULT 0,
    unresolved_ref_count    INTEGER      NOT NULL DEFAULT 0,
    unresolved_refs_json    TEXT         NULL,
    missing_targets_json    TEXT         NULL,
    summary_json            TEXT         NULL,
    created_at              TIMESTAMPTZ  NOT NULL DEFAULT CURRENT_TIMESTAMP
);

