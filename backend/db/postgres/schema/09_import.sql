    id,
    source_name,
    source_kind,
    created_by,
    contract_version,
    source_hash_sha256,
    item_count,
    flavour_count,
    explicit_relation_count,
    raw_prerequisite_count,
    missing_target_count,
    stub_count,
    unresolved_ref_count,
    unresolved_refs_json,
    missing_targets_json,
    summary_json,
    created_at
FROM ranked
WHERE rn = 1;

