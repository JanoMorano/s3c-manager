CREATE OR REPLACE VIEW data.v_c3_entity_membership_matrix AS
SELECT
    entity_kind,
    entity_uuid,
    BOOL_OR(spiral_code = 'Spiral_4') AS in_spiral_4,
    BOOL_OR(spiral_code = 'Spiral_5') AS in_spiral_5,
    BOOL_OR(spiral_code = 'Spiral_6') AS in_spiral_6,
    BOOL_OR(spiral_code = 'Spiral_7') AS in_spiral_7,
    ARRAY_AGG(spiral_code ORDER BY spiral_code) AS spiral_codes,
    MAX(updated_at) AS last_membership_update
FROM data.c3_entity_spiral_membership
GROUP BY entity_kind, entity_uuid;

