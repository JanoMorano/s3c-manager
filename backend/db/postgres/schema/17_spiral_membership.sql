INSERT INTO data.ref_spiral_baseline (spiral_code, spiral_label, is_active, notes)
VALUES
    ('Spiral_4', 'Spiral 4', FALSE, 'Historical FMN spiral baseline; inactive until explicitly enabled.'),
    ('Spiral_5', 'Spiral 5', FALSE, 'FMN Spiral 5 baseline used by Air C2 PDF parity work.'),
    ('Spiral_6', 'Spiral 6 (current baseline)', TRUE, 'Current active baseline for existing C3 seed data.'),
    ('Spiral_7', 'Spiral 7', FALSE, 'Future/imported FMN spiral baseline.')
ON CONFLICT (spiral_code) DO UPDATE SET
    spiral_label = EXCLUDED.spiral_label,
    notes = EXCLUDED.notes;

CREATE TABLE IF NOT EXISTS data.c3_entity_spiral_membership (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    entity_kind        VARCHAR(50)  NOT NULL,
    entity_uuid        VARCHAR(100) NOT NULL,
    spiral_code        VARCHAR(20)  NOT NULL REFERENCES data.ref_spiral_baseline(spiral_code),
    status_in_spiral   VARCHAR(50)  NULL,
    ss_overall_status  VARCHAR(100) NULL,
    ss_baseline_status VARCHAR(100) NULL,
    item_status        VARCHAR(50)  NULL,
    source_run_id      BIGINT NULL REFERENCES data.c3_entity_import_run(id) ON DELETE SET NULL,
    created_at         TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at         TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_c3_entity_spiral_membership_kind
        CHECK (entity_kind IN ('application', 'data_object', 'c3_service', 'technology_interaction', 'capability')),
    CONSTRAINT uq_c3_entity_spiral_membership
        UNIQUE (entity_kind, entity_uuid, spiral_code)
);

CREATE INDEX IF NOT EXISTS ix_c3_membership_entity
    ON data.c3_entity_spiral_membership(entity_kind, entity_uuid);

CREATE INDEX IF NOT EXISTS ix_c3_membership_spiral
    ON data.c3_entity_spiral_membership(spiral_code);

