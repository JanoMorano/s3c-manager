CREATE OR REPLACE FUNCTION parse_seed_timestamptz(p_value TEXT)
RETURNS TIMESTAMPTZ
LANGUAGE sql
IMMUTABLE
AS $$
    SELECT CASE
        WHEN p_value IS NULL OR btrim(p_value) = '' THEN NULL
        ELSE p_value::timestamp AT TIME ZONE 'UTC'
    END
$$;

