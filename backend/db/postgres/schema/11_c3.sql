CREATE OR REPLACE VIEW v_c3technologyinteractionlinkreport AS
WITH service_raw AS (
    SELECT ti.id, btrim(value) AS ref_value
    FROM c3_technology_interaction ti
    CROSS JOIN LATERAL regexp_split_to_table(
        regexp_replace(concat_ws(',', ti.services_1_raw, ti.services_2_raw, ti.services_3_raw), E'[\\r\\n;]+', ',', 'g'),
        ','
    ) AS value
    WHERE btrim(value) <> ''
),
application_raw AS (
    SELECT ti.id, btrim(value) AS ref_value
    FROM c3_technology_interaction ti
    CROSS JOIN LATERAL regexp_split_to_table(
        regexp_replace(concat_ws(',', ti.applications_1_raw, ti.applications_2_raw), E'[\\r\\n;]+', ',', 'g'),
        ','
    ) AS value
    WHERE btrim(value) <> ''
),
data_object_raw AS (
    SELECT ti.id, btrim(value) AS ref_value
    FROM c3_technology_interaction ti
    CROSS JOIN LATERAL regexp_split_to_table(
        regexp_replace(coalesce(ti.data_objects_raw, ''), E'[\\r\\n;]+', ',', 'g'),
        ','
    ) AS value
    WHERE btrim(value) <> ''
),
service_agg AS (
    SELECT
        r.id,
        COUNT(*) AS service_ref_count,
        COUNT(s.id) AS matched_service_count,
        string_agg(CASE WHEN s.id IS NULL THEN r.ref_value END, ', ' ORDER BY r.ref_value) AS unresolved_service_refs
    FROM service_raw r
    LEFT JOIN c3_service s
      ON s.service_code = r.ref_value OR s.uuid = r.ref_value
    GROUP BY r.id
),
application_agg AS (
    SELECT
        r.id,
        COUNT(*) AS application_ref_count,
        COUNT(a.id) AS matched_application_count,
        string_agg(CASE WHEN a.id IS NULL THEN r.ref_value END, ', ' ORDER BY r.ref_value) AS unresolved_application_refs
    FROM application_raw r
    LEFT JOIN c3_application a
      ON a.application_code = r.ref_value OR a.uuid = r.ref_value
    GROUP BY r.id
),
data_object_agg AS (
    SELECT
        r.id,
        COUNT(*) AS data_object_ref_count,
        COUNT(d.id) AS matched_data_object_count,
        string_agg(CASE WHEN d.id IS NULL THEN r.ref_value END, ', ' ORDER BY r.ref_value) AS unresolved_data_object_refs
    FROM data_object_raw r
    LEFT JOIN c3_data_object d
      ON d.data_object_code = r.ref_value OR d.uuid = r.ref_value
    GROUP BY r.id
),
service_links AS (
    SELECT
        l.technology_interaction_id AS id,
        jsonb_agg(
            jsonb_build_object('id', s.id, 'code', s.service_code, 'uuid', s.uuid, 'title', s.title)
            ORDER BY s.service_code
        )::text AS linked_services_json
    FROM c3_technology_interaction_service_link l
    JOIN c3_service s ON s.id = l.c3_service_id
    GROUP BY l.technology_interaction_id
),
application_links AS (
    SELECT
        l.technology_interaction_id AS id,
        jsonb_agg(
            jsonb_build_object('id', a.id, 'code', a.application_code, 'uuid', a.uuid, 'title', a.title)
            ORDER BY a.application_code
        )::text AS linked_applications_json
    FROM c3_technology_interaction_application_link l
    JOIN c3_application a ON a.id = l.c3_application_id
    GROUP BY l.technology_interaction_id
),
data_object_links AS (
    SELECT
        l.technology_interaction_id AS id,
        jsonb_agg(
            jsonb_build_object('id', d.id, 'code', d.data_object_code, 'uuid', d.uuid, 'title', d.title)
            ORDER BY d.data_object_code
        )::text AS linked_data_objects_json
    FROM c3_technology_interaction_data_object_link l
    JOIN c3_data_object d ON d.id = l.c3_data_object_id
    GROUP BY l.technology_interaction_id
)
SELECT
    ti.id,
    ti.technology_interaction_code,
    ti.uuid,
    ti.title,
    ti.item_status,
    ti.technology_interaction_type,
    ti.technology_interaction_maturity,
    COALESCE(sa.service_ref_count, 0) AS service_ref_count,
    COALESCE(sa.matched_service_count, 0) AS matched_service_count,
    sl.linked_services_json,
    sa.unresolved_service_refs,
    COALESCE(aa.application_ref_count, 0) AS application_ref_count,
    COALESCE(aa.matched_application_count, 0) AS matched_application_count,
    al.linked_applications_json,
    aa.unresolved_application_refs,
    COALESCE(da.data_object_ref_count, 0) AS data_object_ref_count,
    COALESCE(da.matched_data_object_count, 0) AS matched_data_object_count,
    dl.linked_data_objects_json,
    da.unresolved_data_object_refs,
    ti.synced_at,
    ti.updated_at
FROM c3_technology_interaction ti
LEFT JOIN service_agg sa ON sa.id = ti.id
LEFT JOIN service_links sl ON sl.id = ti.id
LEFT JOIN application_agg aa ON aa.id = ti.id
LEFT JOIN application_links al ON al.id = ti.id
LEFT JOIN data_object_agg da ON da.id = ti.id
LEFT JOIN data_object_links dl ON dl.id = ti.id;

