CREATE OR REPLACE VIEW v_c3capabilitycompleteness AS
SELECT
    c.uuid,
    c.external_id,
    c.title,
    c.item_type,
    c.item_status,
    COALESCE(app.cnt, 0) AS app_count,
    COALESCE(do_.cnt, 0) AS data_object_count,
    COALESCE(tin.cnt, 0) AS tin_count,
    COALESCE(svc.cnt, 0) AS c3_service_count,
    COALESCE(scm.cnt, 0) AS service_mapping_count,
    CASE WHEN COALESCE(app.cnt, 0) > 0 THEN TRUE ELSE FALSE END AS has_app,
    CASE WHEN COALESCE(do_.cnt, 0) > 0 THEN TRUE ELSE FALSE END AS has_data_object,
    CASE WHEN COALESCE(tin.cnt, 0) > 0 THEN TRUE ELSE FALSE END AS has_tin,
    CASE WHEN COALESCE(svc.cnt, 0) > 0 THEN TRUE ELSE FALSE END AS has_c3_service,
    CASE WHEN COALESCE(scm.cnt, 0) > 0 THEN TRUE ELSE FALSE END AS has_service_mapping,
    CASE
        WHEN COALESCE(app.cnt, 0) > 0
         AND COALESCE(do_.cnt, 0) > 0
         AND COALESCE(tin.cnt, 0) > 0 THEN 'complete'
        WHEN COALESCE(app.cnt, 0)
           + COALESCE(do_.cnt, 0)
           + COALESCE(tin.cnt, 0)
           + COALESCE(svc.cnt, 0)
           + COALESCE(scm.cnt, 0) > 0 THEN 'partial'
        ELSE 'incomplete'
    END AS completeness_status
FROM c3_taxonomy c
LEFT JOIN (
    SELECT capability_uuid, COUNT(*) AS cnt
    FROM c3_capability_application_link
    GROUP BY capability_uuid
) app ON app.capability_uuid = c.uuid
LEFT JOIN (
    SELECT capability_uuid, COUNT(*) AS cnt
    FROM c3_capability_data_object_link
    GROUP BY capability_uuid
) do_ ON do_.capability_uuid = c.uuid
LEFT JOIN (
    SELECT capability_uuid, COUNT(*) AS cnt
    FROM c3_capability_tin_link
    GROUP BY capability_uuid
) tin ON tin.capability_uuid = c.uuid
LEFT JOIN (
    SELECT capability_uuid, COUNT(*) AS cnt
    FROM c3_capability_c3_service_link
    GROUP BY capability_uuid
) svc ON svc.capability_uuid = c.uuid
LEFT JOIN (
    SELECT c3_uuid, COUNT(*) AS cnt
    FROM service_c3_mapping
    GROUP BY c3_uuid
) scm ON scm.c3_uuid = c.uuid;

CREATE OR REPLACE VIEW v_servicepublishreadiness AS
WITH active_flavours AS (
    SELECT
        sf.service_id,
        COUNT(*) AS active_flavour_count
    FROM service_flavour sf
    WHERE sf.is_deleted = FALSE
      AND lower(COALESCE(sf.flavour_status_code, '')) IN ('available', 'active')
    GROUP BY sf.service_id
),
relation_counts AS (
    SELECT
        sc.id AS service_id,
        COUNT(sr.id) AS relation_count,
        SUM(CASE WHEN sr.relation_type_code IN ('depends_on', 'prerequisite', 'underlying') THEN 1 ELSE 0 END) AS dependency_relation_count
    FROM service_catalog sc
    LEFT JOIN service_relation sr
      ON sr.is_deleted = FALSE
     AND (sr.from_service_id = sc.id OR sr.to_service_id = sc.id)
    WHERE sc.is_deleted = FALSE
    GROUP BY sc.id
),
primary_mapping AS (
    SELECT
        scm.service_id,
        COUNT(*) AS primary_mapping_count,
        MAX(CASE WHEN scm.is_primary = TRUE THEN scm.c3_uuid END) AS primary_c3_uuid
    FROM service_c3_mapping scm
    JOIN service_catalog sc
      ON sc.id = scm.service_id
     AND sc.is_deleted = FALSE
    WHERE scm.is_primary = TRUE
    GROUP BY scm.service_id
)
SELECT
    sc.id AS service_pk,
    sc.service_id,
    sc.title,
    sc.service_status_code AS service_status,
    pm.primary_mapping_count,
    pm.primary_c3_uuid,
    cap.title AS primary_c3_title,
    cap.external_id AS primary_c3_code,
    comp.completeness_status AS primary_c3_completeness_status,
    comp.app_count AS primary_c3_app_count,
    comp.data_object_count AS primary_c3_data_object_count,
    comp.tin_count AS primary_c3_tin_count,
    comp.c3_service_count AS primary_c3_c3_service_count,
    comp.service_mapping_count AS primary_c3_service_mapping_count,
    COALESCE(af.active_flavour_count, 0) AS active_flavour_count,
    COALESCE(rc.relation_count, 0) AS relation_count,
    COALESCE(rc.dependency_relation_count, 0) AS dependency_relation_count,
    CASE WHEN COALESCE(pm.primary_mapping_count, 0) = 1 AND pm.primary_c3_uuid IS NOT NULL THEN TRUE ELSE FALSE END AS has_single_primary_mapping,
    CASE WHEN comp.completeness_status = 'complete' THEN TRUE ELSE FALSE END AS has_complete_primary_capability,
    CASE WHEN COALESCE(af.active_flavour_count, 0) > 0 THEN TRUE ELSE FALSE END AS has_active_flavour,
    CASE
        WHEN COALESCE(pm.primary_mapping_count, 0) = 1
         AND pm.primary_c3_uuid IS NOT NULL
         AND comp.completeness_status = 'complete'
         AND COALESCE(af.active_flavour_count, 0) > 0
        THEN TRUE ELSE FALSE
    END AS is_publishable
FROM service_catalog sc
LEFT JOIN primary_mapping pm
  ON pm.service_id = sc.id
LEFT JOIN c3_taxonomy cap
  ON cap.uuid = pm.primary_c3_uuid
LEFT JOIN v_c3capabilitycompleteness comp
  ON comp.uuid = pm.primary_c3_uuid
LEFT JOIN active_flavours af
  ON af.service_id = sc.id
LEFT JOIN relation_counts rc
  ON rc.service_id = sc.id
WHERE sc.is_deleted = FALSE
  AND sc.is_stub = FALSE;

CREATE OR REPLACE VIEW v_c3dashboardsummary AS
WITH mapped AS (
    SELECT DISTINCT c3_uuid
    FROM service_c3_mapping
    WHERE c3_uuid IS NOT NULL
),
mapping_counts AS (
    SELECT c3_uuid, COUNT(*) AS mapping_count
    FROM service_c3_mapping
    WHERE c3_uuid IS NOT NULL
    GROUP BY c3_uuid
)
SELECT
    COUNT(*) AS total_items,
    SUM(CASE WHEN m.c3_uuid IS NOT NULL THEN 1 ELSE 0 END) AS mapped_items,
    SUM(CASE WHEN m.c3_uuid IS NULL THEN 1 ELSE 0 END) AS unmapped_items,
    COALESCE(SUM(mc.mapping_count), 0) AS total_mappings,
    COUNT(DISTINCT c.item_type) AS item_type_count,
    COUNT(DISTINCT c.application) AS application_count
FROM c3_taxonomy c
LEFT JOIN mapped m
  ON m.c3_uuid = c.uuid
LEFT JOIN mapping_counts mc
  ON mc.c3_uuid = c.uuid;

