CREATE TABLE IF NOT EXISTS app_config (
    id           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    config_key   VARCHAR(100) NOT NULL UNIQUE,
    config_value TEXT NULL,
    config_type  VARCHAR(20) NULL DEFAULT 'string'
                 CHECK (config_type IN ('string', 'json', 'boolean', 'number') OR config_type IS NULL),
    description  VARCHAR(500) NULL,
    is_sensitive BOOLEAN NOT NULL DEFAULT FALSE,
    updated_at   TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_by   VARCHAR(200) NULL
);

CREATE TABLE IF NOT EXISTS users (
    id              INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username        VARCHAR(100) NOT NULL UNIQUE,
    display_name    VARCHAR(200) NULL,
    email           VARCHAR(255) NULL,
    role            VARCHAR(20)  NOT NULL DEFAULT 'viewer'
                    CHECK (role IN ('viewer', 'editor', 'admin')),
    is_active       BOOLEAN NOT NULL DEFAULT TRUE,
    preferred_lang  VARCHAR(5)  NULL DEFAULT 'cz',
    preferred_theme VARCHAR(10) NULL DEFAULT 'dark',
    auth_provider   VARCHAR(20) NOT NULL DEFAULT 'local'
                    CHECK (auth_provider IN ('local', 'ad')),
    external_principal VARCHAR(255) NULL,
    password_hash   VARCHAR(200) NULL,
    given_name      VARCHAR(100) NULL,
    surname         VARCHAR(100) NULL,
    phone           VARCHAR(50) NULL,
    department      VARCHAR(200) NULL,
    avatar_color    VARCHAR(20) NULL,
    last_login_at   TIMESTAMPTZ NULL,
    last_sso_login_at TIMESTAMPTZ NULL,
    last_login_ip   VARCHAR(50) NULL,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS refresh_tokens (
    id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    token_hash  VARCHAR(500) NOT NULL,
    expires_at  TIMESTAMPTZ NOT NULL,
    revoked_at  TIMESTAMPTZ NULL,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS audit_log (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_name     VARCHAR(100) NOT NULL,
    record_id      INTEGER NOT NULL,
    record_label   VARCHAR(255) NULL,
    action         VARCHAR(20) NOT NULL CHECK (
                     action IN (
                       'INSERT','UPDATE','DELETE','SOFT_DELETE',
                       'RESTORE','SP_IMPORT','JSON_IMPORT','SCORE_RECALC'
                     )
                   ),
    old_values     TEXT NULL,
    new_values     TEXT NULL,
    changed_fields TEXT NULL,
    performed_by   VARCHAR(200) NULL,
    performed_at   TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    client_ip      VARCHAR(50) NULL,
    user_agent     VARCHAR(500) NULL
);

