CREATE OR REPLACE FUNCTION cleanup_expired_tokens()
RETURNS TABLE(deleted_count INTEGER)
LANGUAGE plpgsql
AS $$
DECLARE
    affected_rows INTEGER;
BEGIN
    DELETE FROM refresh_tokens
    WHERE expires_at < CURRENT_TIMESTAMP OR revoked_at IS NOT NULL;

    GET DIAGNOSTICS affected_rows = ROW_COUNT;
    RETURN QUERY SELECT affected_rows;
END;
$$;

CREATE OR REPLACE FUNCTION trim_audit_log(p_retention_days INTEGER DEFAULT 365)
RETURNS TABLE(deleted_count INTEGER, cutoff_date TIMESTAMPTZ)
LANGUAGE plpgsql
AS $$
DECLARE
    affected_rows INTEGER;
    cutoff TIMESTAMPTZ;
BEGIN
    cutoff := CURRENT_TIMESTAMP - make_interval(days => p_retention_days);

    DELETE FROM audit_log
    WHERE performed_at < cutoff;

    GET DIAGNOSTICS affected_rows = ROW_COUNT;
    RETURN QUERY SELECT affected_rows, cutoff;
END;
$$;

